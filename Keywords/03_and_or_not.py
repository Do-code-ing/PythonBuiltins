# and, or, not
# 논리연산자가 아니다.

# and
# 두 피연산자가 모두 True인 경우에만 True가 된다.
print(True and True)
# True

print(True and False)
print(False and True)
print(False and False)
# False

# or
# 두 피연산자 중 하나라도 True인 경우에 True가 된다.
print(True or True)
print(True or False)
print(False or True)
# True

print(False or False)
# False

# not
# 진리 값을 반전하는 데 사용된다.
print(not True)
# False

print(not False)
# True
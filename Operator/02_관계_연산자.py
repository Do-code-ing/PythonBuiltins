# 관계 연산자 (Relational Operator)
# = 비교 연산자 (Comparison operators)
# 피연산자 간의 비교를 한다.
# 피연산자가 다른 것보다 큰지, 작은 지, 같은지, 또는 이들의 조합인지를 맞다면 True, 아니라면 False를 반환한다.
# >, <, >=, <=, ==, != 이 있다.

# 큰지, 작은 지 : Greater than(>), Less than(<)
print(4>3)
# True
print(4<3)
# False

# 크거나 같은지, 작거나 같은지 : Greater than or equal to(>=), Less than or equal to(<=)
print(1>=1)
print(1<=1)
# True

# 같은지 : Equal to(= =)
print(1==1.0)
# True
print(1==1.001)
# False

# 같지 않은지 : Not equal to(!=)
print(1!=1.1)
# True
print(1!=1.0)
# False
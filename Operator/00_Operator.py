# 연산자 (Operator)
# Python 연산자는 하나 이상의 피연산자에 대해 연산을 수행하는 기호다.
# 피연산자는 연산을 수행하는 변수 또는 값이다.

# 연산자 종류

# 01 산수 연산자 (Arithmetic Operator)
# 02 관계 연산자 (Relational Operator)
# 03 할당 연산자 (Assignment Operator)
# 04 논리 연산자 (Logical Operator)
# 05 멤버십 연산자 (Membership Operator)
# 06 ID 연산자 (Identity Operator)
# 07 비트 연산자 (Bitwise Operator)
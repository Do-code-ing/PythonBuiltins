# 산수 연산자 (Arithmetic Operators)
# 기본 수학을 위한 연산자다.
# +, -, *, /, **, //, % 가 있다.

# 덧셈 : addition(+)
print(3+4)
# 7

# 뺄셈 : subtraction(-)
print(3-4)
# -1

# 곱셈 : Multiplication(*)
print(3*4)
# 12

# 나눗셈 : Division(/)
print(3/4)
# 0.75

# 거듭제곱법 (누승법) : Exponentiation(**)
print(3**4)
# 81

# 나눗셈 후 몫 구하기 : Floor Division(//)
print(4//3)
# 1

# 나눗셈 후 나머지 구하기 : Modulus(%)
print(4%3)
# 1